/**
 * Random number generation, ported from reference/src/z-rand.c (Angband
 * 4.2.6).
 *
 * The upstream implementation is a single global WELL1024a stream with a
 * "quick" LCRNG mode and a seed-swap trick for stable seeds (flavors,
 * randarts). This port makes the generator instantiable so the engine can
 * run named, independently seeded streams instead of swapping seeds on one
 * global; each individual stream is bit-for-bit faithful to upstream.
 *
 * All arithmetic reproduces C uint32 semantics exactly (Math.imul for
 * wrapping multiplication, >>> 0 for unsigned truncation, Math.trunc for
 * C integer division). Golden vectors generated by an independent Python
 * oracle (scripts/gen-rng-vectors.py) pin the outputs.
 */

/**
 * Assumed maximum dungeon level, used for object/monster creation math.
 */
export const MAX_RAND_DEPTH = 128;

/**
 * The number of 32-bit integers worth of WELL seed state.
 */
export const RAND_DEG = 32;

/**
 * Random aspects used by damcalc, mBonusCalc, and randcalc.
 * Order mirrors the upstream enum: MINIMISE, AVERAGE, MAXIMISE, EXTREMIFY,
 * RANDOMISE.
 */
export type Aspect =
  | "minimise"
  | "average"
  | "maximise"
  | "extremify"
  | "randomise";

/**
 * A strategy for making a dice roll: base + XdY + BONUS, where mBonus is
 * used in a tricky way to determine BONUS.
 */
export interface RandomValue {
  base: number;
  dice: number;
  sides: number;
  mBonus: number;
}

/**
 * A random chance of success, such as 8 in 125 (6.4 percent).
 */
export interface RandomChance {
  numerator: number;
  denominator: number;
}

/**
 * Serializable snapshot of a generator, for savefiles.
 */
export interface RngState {
  quick: boolean;
  value: number;
  state: number[];
  stateI: number;
  fixed: boolean;
  fixval: number;
}

/** LCRNG(X) = X * 1103515245 + 12345, in uint32. */
function lcrng(x: number): number {
  return (Math.imul(x, 1103515245) + 12345) >>> 0;
}

