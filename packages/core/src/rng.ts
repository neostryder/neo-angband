/**
 * A single RNG stream: WELL1024a (the upstream "complex" RNG) with an
 * optional LCRNG "quick" mode, faithful to z-rand.c.
 */
export class Rng {
  private state = new Uint32Array(RAND_DEG);
  private stateI = 0;
  private quick: boolean;
  /** Rand_value: the quick-mode LCRNG state. */
  private value: number;
  private fixed = false;
  private fixval = 0;

  constructor(seed: number, opts?: { quick?: boolean }) {
    this.quick = opts?.quick ?? false;
    this.value = seed >>> 0;
    if (!this.quick) {
      this.stateInit(seed);
    }
  }

  /**
   * Rand_state_init: seed the WELL table via LCRNG propagation, then cycle
   * the table ten times per degree.
   */
  stateInit(seed: number): void {
    this.state.fill(0);
    this.stateI = 0;
    this.state[0] = seed >>> 0;
    for (let i = 1; i < RAND_DEG; i++) {
      this.state[i] = lcrng(this.state[i - 1] as number);
    }
    for (let i = 0; i < RAND_DEG * 10; i++) {
      const j = (this.stateI + 1) % RAND_DEG;
      this.state[j] =
        (((this.state[j] as number) + (this.state[this.stateI] as number)) >>>
          0);
      this.stateI = j;
    }
  }

  /** WELLRNG1024a step. */
  private well(): number {
    const s = this.state;
    const i = this.stateI;
    const v0 = s[i] as number;
    const vm1 = s[(i + 3) & 31] as number;
    const vm2 = s[(i + 24) & 31] as number;
    const vm3 = s[(i + 10) & 31] as number;
    const vrm1 = s[(i + 31) & 31] as number;
    const z0 = vrm1;
    const z1 = (v0 ^ (vm1 ^ (vm1 >>> 8))) >>> 0;
    const z2 = ((vm2 ^ ((vm2 << 19) >>> 0)) ^ (vm3 ^ ((vm3 << 14) >>> 0))) >>> 0;
    s[i] = (z1 ^ z2) >>> 0;
    const newV0 =
      ((z0 ^ ((z0 << 11) >>> 0)) ^
        (z1 ^ ((z1 << 7) >>> 0)) ^
        (z2 ^ ((z2 << 13) >>> 0))) >>>
      0;
    s[(i + 31) & 31] = newV0;
    this.stateI = (i + 31) & 31;
    return this.state[this.stateI] as number;
  }

  /**
   * Rand_div: extract a random number from 0 to m - 1 via unbiased
   * partitioned division of 28-bit values.
   */
  randDiv(m: number): number {
    if (m > 0x10000000) {
      throw new RangeError(`Rand_div: m must be <= 0x10000000, got ${m}`);
    }
    if (m <= 1) return 0;
    if (this.fixed) {
      // C evaluates rand_fixval * 1000 * (m - 1) in uint32 with wrap.
      let t = Math.imul(this.fixval, 1000) >>> 0;
      t = Math.imul(t, m - 1) >>> 0;
      return Math.trunc(t / (100 * 1000));
    }
    const n = Math.trunc(0x10000000 / m);
    for (;;) {
      let r: number;
      if (this.quick) {
        this.value = lcrng(this.value);
        r = Math.trunc(((this.value >>> 4) & 0x0fffffff) / n);
      } else {
        r = Math.trunc(((this.well() >>> 4) & 0x0fffffff) / n);
      }
      if (r < m) return r;
    }
  }

  /** randint0(M): 0 <= X < M, uniform. */
  randint0(m: number): number {
    return this.randDiv(m);
  }

  /** randint1(M): 1 <= X <= M, uniform. */
  randint1(m: number): number {
    return this.randDiv(m) + 1;
  }

  /** one_in_(x): true one time in x. */
  oneIn(x: number): boolean {
    return this.randint0(x) === 0;
  }

  /** rand_range: A <= X <= B, uniform. */
  randRange(a: number, b: number): number {
    if (a === b) return a;
    if (a > b) {
      throw new RangeError(`rand_range: A (${a}) must be <= B (${b})`);
    }
    return a + this.randDiv(1 + b - a);
  }

  /** rand_spread(A, D): A - D <= X <= A + D, uniform. */
  randSpread(a: number, d: number): number {
    return a + this.randint0(1 + d + d) - d;
  }

  /**
   * Rand_normal: a random integer from a (table-driven pseudo-)normal
   * distribution with the given mean and standard deviation.
   */
  randNormal(mean: number, stand: number): number {
    if (stand < 1) return mean;
    const tmp = this.randint0(32768);
    let low = 0;
    let high = 256;
    while (low < high) {
      const mid = (low + high) >> 1;
      if ((RAND_NORMAL_TABLE[mid] as number) < tmp) {
        low = mid + 1;
      } else {
        high = mid;
      }
    }
    const offset = Math.trunc((stand * low) / 64);
    if (this.oneIn(2)) return mean - offset;
    return mean + offset;
  }

  /**
   * Rand_sample: an integer from a distribution with known mean and
   * approximate bounds; standU/standL are 10x the assumed standard
   * deviations to each bound.
   */
  randSample(
    mean: number,
    upper: number,
    lower: number,
    standU: number,
    standL: number,
  ): number {
    let pick = this.randNormal(0, 1000);
    if (pick > 0) {
      pick *= upper - mean;
      pick = Math.trunc(pick / (100 * standU));
    } else if (pick < 0) {
      pick *= mean - lower;
      pick = Math.trunc(pick / (100 * standL));
    }
    return mean + pick;
  }

  /** damroll: sum of `num` rolls of a `sides`-sided die. */
  damroll(num: number, sides: number): number {
    if (sides <= 0) return 0;
    let sum = 0;
    for (let i = 0; i < num; i++) sum += this.randint1(sides);
    return sum;
  }

  /** Division that rounds up or down randomly, weighted by the remainder. */
  private simulateDivision(dividend: number, divisor: number): number {
    let quotient = Math.trunc(dividend / divisor);
    const remainder = dividend % divisor;
    if (this.randint0(divisor) < remainder) quotient++;
    return quotient;
  }

  /**
   * m_bonus: an enchantment bonus from a normal distribution whose mean
   * approaches max as level approaches MAX_RAND_DEPTH.
   */
  mBonus(max: number, level: number): number {
    if (level >= MAX_RAND_DEPTH) level = MAX_RAND_DEPTH - 1;
    const bonus = this.simulateDivision(max * level, MAX_RAND_DEPTH);
    const stand = this.simulateDivision(max, 4);
    const value = this.randNormal(bonus, stand);
    if (value < 0) return 0;
    if (value > max) return max;
    return value;
  }

  /** m_bonus_calc: aspect-parameterized companion of mBonus. */
  mBonusCalc(max: number, level: number, aspect: Aspect): number {
    switch (aspect) {
      case "extremify":
      case "maximise":
        return max;
      case "randomise":
        return this.mBonus(max, level);
      case "minimise":
        return 0;
      case "average":
        return Math.trunc((max * level) / MAX_RAND_DEPTH);
    }
  }

  /** damcalc: aspect-parameterized companion of damroll. */
  damcalc(num: number, sides: number, aspect: Aspect): number {
    switch (aspect) {
      case "maximise":
      case "extremify":
        return num * sides;
      case "randomise":
        return this.damroll(num, sides);
      case "minimise":
        return num;
      case "average":
        return Math.trunc((num * (sides + 1)) / 2);
    }
  }

  /** randcalc: evaluate a RandomValue under an aspect. */
  randcalc(v: RandomValue, level: number, aspect: Aspect): number {
    if (aspect === "extremify") {
      const min = this.randcalc(v, level, "minimise");
      const max = this.randcalc(v, level, "maximise");
      return Math.abs(min) > Math.abs(max) ? min : max;
    }
    const dmg = this.damcalc(v.dice, v.sides, aspect);
    const bonus = this.mBonusCalc(v.mBonus, level, aspect);
    return v.base + dmg + bonus;
  }

  /** Whether `test` lies within the RandomValue's possible range. */
  randcalcValid(v: RandomValue, test: number): boolean {
    if (test < this.randcalc(v, 0, "minimise")) return false;
    if (test > this.randcalc(v, 0, "maximise")) return false;
    return true;
  }

  /** Whether the RandomValue actually varies. */
  randcalcVaries(v: RandomValue): boolean {
    return (
      this.randcalc(v, 0, "minimise") !== this.randcalc(v, 0, "maximise")
    );
  }

  /** random_chance_check: roll a numerator-in-denominator chance. */
  randomChanceCheck(c: RandomChance): boolean {
    return this.randint0(c.denominator) >= c.denominator - c.numerator;
  }

  /**
   * rand_fix: make randDiv return a fixed percentage of its maximum rather
   * than a random value (upstream test hook).
   */
  randFix(val: number): void {
    this.fixed = true;
    this.fixval = val >>> 0;
  }

  /** Undo randFix (upstream never needs this; tests do). */
  randUnfix(): void {
    this.fixed = false;
    this.fixval = 0;
  }

  /** Snapshot the full generator state for savefiles. */
  getState(): RngState {
    return {
      quick: this.quick,
      value: this.value,
      state: Array.from(this.state),
      stateI: this.stateI,
      fixed: this.fixed,
      fixval: this.fixval,
    };
  }

  /** Restore a snapshot taken with getState(). */
  setState(s: RngState): void {
    if (s.state.length !== RAND_DEG) {
      throw new RangeError(
        `RngState.state must have ${RAND_DEG} entries, got ${s.state.length}`,
      );
    }
    this.quick = s.quick;
    this.value = s.value >>> 0;
    this.state = Uint32Array.from(s.state);
    this.stateI = s.stateI;
    this.fixed = s.fixed;
    this.fixval = s.fixval;
  }
}

/**
 * random_chance_scaled: rescale a chance ratio to a new denominator
 * (pure arithmetic; no RNG state involved).
 */
export function randomChanceScaled(c: RandomChance, scale: number): number {
  return Math.trunc((scale * c.numerator) / c.denominator);
}

/**
 * A set of named, independently seeded RNG streams. This replaces the
 * upstream seed-swap trick (seed_flavor, seed_randart) with first-class
 * streams; each stream is a faithful WELL1024a generator.
 */
export class RngStreams {
  private streams = new Map<string, Rng>();

  /** Create (or re-seed) the named stream. */
  create(name: string, seed: number): Rng {
    const rng = new Rng(seed);
    this.streams.set(name, rng);
    return rng;
  }

  /** Get a stream; throws if it was never created. */
  get(name: string): Rng {
    const rng = this.streams.get(name);
    if (!rng) throw new Error(`RNG stream not created: ${name}`);
    return rng;
  }

  has(name: string): boolean {
    return this.streams.has(name);
  }

  names(): string[] {
    return Array.from(this.streams.keys());
  }

  /** Snapshot every stream, for savefiles. */
  getState(): Record<string, RngState> {
    const out: Record<string, RngState> = {};
    for (const [name, rng] of this.streams) out[name] = rng.getState();
    return out;
  }

  /** Restore a snapshot taken with getState(). */
  setState(states: Record<string, RngState>): void {
    this.streams.clear();
    for (const [name, s] of Object.entries(states)) {
      const rng = new Rng(0);
      rng.setState(s);
      this.streams.set(name, rng);
    }
  }
}
